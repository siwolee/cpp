int find_pair(std::vector<ipair>& pairs, int v) {
  for (int i = 0; i < pairs.size(); ++i) {
    if (pairs[i].second == v) {
      return pairs[i].first;
    }
  }
  return v;
}

void print_vec(std::vector<int>& v) {
  for (int i = 0; i < v.size(); ++i) {
    std::cout << v[i] << " ";
  }
  std::cout << "\n";
}

vpair set_pair(std::vector<int>& v) {
  int mid = v.size() / 2;
  std::vector<int> lv(mid);
  std::vector<int> sv(mid);
  for (int i = 0; i < mid; i++) {
    lv[i] = v[i];
    sv[i] = v[i + mid];
    if (lv[i] < sv[i]) {
      std::swap(lv[i], sv[i]);
    }
  }
  return std::make_pair(lv, sv);
}

std::vector<int> insertion(std::vector<int>& cv, std::vector<int> sv, int idx) {
  // 1 -> 3 -> 2 -> 5 -> 4 -> 11 -> 10 -> 9 -> 8 -> 7 -> 6
  if (jacob[idx - 1] >= cv.size()) {
    return cv;
  }
  std::size_t start = sv.size() > jacob[idx] ? jacob[idx] : sv.size();
  if (idx == 1) {
    cv.insert(cv.begin(), sv[0]);
    return insertion(cv, sv, idx + 1);
  }
  std::cout << "std::pow(2, idx) - 1 : " << std::pow(2, idx) - 1 << ", ";
  size_t end =
      std::pow(2, idx) - 1 > cv.size() ? cv.size() : std::pow(2, idx) - 1;
  for (std::size_t i = start - 1; i >= jacob[idx - 1]; --i) {
    cv.insert(std::lower_bound(cv.begin(), cv.begin() + end, sv[i]), sv[i]);
    if (i == 0) {
      break;
    }
  }
  std::cout << "\n";
  return insertion(cv, sv, idx + 1);
}

std::vector<int> sorted(std::vector<int>& v) {
  int mid = v.size() / 2;
  std::vector<int> _cv;
  std::vector<int> lv(mid);
  std::vector<int> sv(mid);
  std::vector<ipair> pair_vector;
  int odd = -1;
  if (v.size() % 2) {
    odd = v[v.size() - 1];
  }
  if (v.size() < 2) {
    _cv.push_back(v[0]);
    return _cv;
  }
  for (int i = 0; i < mid; i++) {
    lv[i] = v[i];
    sv[i] = v[i + mid];
    if (lv[i] < sv[i]) {
      std::swap(lv[i], sv[i]);
    }
    pair_vector.push_back(std::make_pair(sv[i], lv[i]));
  }
  _cv = sorted(lv);
  std::vector<int> _sv(sv.size());
  for (int i = 0; i < _sv.size(); ++i) {
    _sv[i] = find_pair(pair_vector, _cv[i]);
  }
  if (odd != -1) {
    _sv.push_back(odd);
  }
  std::cout << "_sv: ";
  print_vec(_sv);
  insertion(_cv, _sv, 1);
  std::cout << "_cv: ";
  print_vec(_cv);
  return _cv;
}

